    @Test
    @Transactional
    void putExistingEvent() throws Exception {
        // Initialize the database
        eventRepository.saveAndFlush(event);

        int databaseSizeBeforeUpdate = eventRepository.findAll().size();

        // Update the event
        Event updatedEvent = eventRepository.findById(event.getId()).get();
        // Disconnect from session so that the updates on updatedEvent are not directly saved in db
        em.detach(updatedEvent);
        updatedEvent
            .title(UPDATED_TITLE)
            .location(UPDATED_LOCATION)
            .startTime(UPDATED_START_TIME)
            .endTime(UPDATED_END_TIME)
            .notes(UPDATED_NOTES)
            .sendPushNotification(UPDATED_SEND_PUSH_NOTIFICATION)
            .sendEmailNotification(UPDATED_SEND_EMAIL_NOTIFICATION)
            .notificationTime(UPDATED_NOTIFICATION_TIME)
            .status(UPDATED_STATUS);
        EventDTO eventDTO = eventMapper.toDto(updatedEvent);

        restEventMockMvc
            .perform(
                put(ENTITY_API_URL_ID, eventDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(eventDTO))
            )
            .andExpect(status().isOk());

        // Validate the Event in the database
        List<Event> eventList = eventRepository.findAll();
        assertThat(eventList).hasSize(databaseSizeBeforeUpdate);
        Event testEvent = eventList.get(eventList.size() - 1);
        assertThat(testEvent.getTitle()).isEqualTo(UPDATED_TITLE);
        assertThat(testEvent.getLocation()).isEqualTo(UPDATED_LOCATION);
        assertThat(testEvent.getStartTime()).isEqualTo(UPDATED_START_TIME);
        assertThat(testEvent.getEndTime()).isEqualTo(UPDATED_END_TIME);
        assertThat(testEvent.getNotes()).isEqualTo(UPDATED_NOTES);
        assertThat(testEvent.getSendPushNotification()).isEqualTo(UPDATED_SEND_PUSH_NOTIFICATION);
        assertThat(testEvent.getSendEmailNotification()).isEqualTo(UPDATED_SEND_EMAIL_NOTIFICATION);
        assertThat(testEvent.getNotificationTime()).isEqualTo(UPDATED_NOTIFICATION_TIME);
        assertThat(testEvent.getStatus()).isEqualTo(UPDATED_STATUS);
    }

