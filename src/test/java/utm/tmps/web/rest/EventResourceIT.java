    @Test
    @Transactional
    void createEvent() throws Exception {
        int databaseSizeBeforeCreate = eventRepository.findAll().size();
        // Create the Event
        EventDTO eventDTO = eventMapper.toDto(event);
        restEventMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(eventDTO)))
            .andExpect(status().isCreated());

        // Validate the Event in the database
        List<Event> eventList = eventRepository.findAll();
        assertThat(eventList).hasSize(databaseSizeBeforeCreate + 1);
        Event testEvent = eventList.get(eventList.size() - 1);
        assertThat(testEvent.getTitle()).isEqualTo(DEFAULT_TITLE);
        assertThat(testEvent.getLocation()).isEqualTo(DEFAULT_LOCATION);
        assertThat(testEvent.getStartTime()).isEqualTo(DEFAULT_START_TIME);
        assertThat(testEvent.getEndTime()).isEqualTo(DEFAULT_END_TIME);
        assertThat(testEvent.getNotes()).isEqualTo(DEFAULT_NOTES);
        assertThat(testEvent.getSendPushNotification()).isEqualTo(DEFAULT_SEND_PUSH_NOTIFICATION);
        assertThat(testEvent.getSendEmailNotification()).isEqualTo(DEFAULT_SEND_EMAIL_NOTIFICATION);
        assertThat(testEvent.getNotificationTime()).isEqualTo(DEFAULT_NOTIFICATION_TIME);
        assertThat(testEvent.getStatus()).isEqualTo(DEFAULT_STATUS);
    }

